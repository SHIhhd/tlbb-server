--**********************************
-- 条件检测入口：返回1：条件检测通过，可以继续执行；返回0：条件检测失败，中断后续执行。
--**********************************
function x300300_OnConditionCheck( sceneId, selfId )
	-- 校验使用的物品
	if LuaFnVerifyUsedItem( sceneId, selfId ) ~= 1 then
		return 0
	end

	local checkCreatePet = TryCreatePet(sceneId, selfId, 2);
	if not checkCreatePet or checkCreatePet ~= 1 then
		x300300_NotifyFailTips(sceneId, selfId, "您不能携带更多的珍兽。");
		return 0;
	end

	local itemTblIndex = LuaFnGetItemIndexOfUsedItem( sceneId, selfId );
	local petItem = x300300_g_petList[itemTblIndex];
	if not petItem then
		x300300_NotifyFailTips(sceneId, selfId, "未开放道具，无法使用。");
		return 0;
	end
	
	if petItem.type == 2 then
		if getn(petItem.dataIds) ~= 2 and getn(petItem.dataIds) ~= 4 and petItem.dataIds ~= nil and petItem.dataIds[1].dataId ~= nil then
			x300300_NotifyFailTips(sceneId, selfId, "珍兽笼暂未开放，无法使用");
			return 0;
		end
		--客户端要单独弹出UI
		local BagIndex = LuaFnGetBagIndexOfUsedItem(sceneId, selfId)
		BeginUICommand(sceneId)
			UICommand_AddInt(sceneId,BagIndex);
			UICommand_AddInt(sceneId,itemTblIndex);
			UICommand_AddInt(sceneId,getn(petItem.dataIds));--2 or 4
			UICommand_AddString(sceneId,GetPetName(petItem.dataIds[1].dataId))--以第一个为准
		EndUICommand(sceneId)
		DispatchUICommand(sceneId,selfId, 171718)
		return 0
	end
	return 1
end

--**********************************
--消耗检测及处理入口，负责消耗的检测和执行：
--返回1：消耗处理通过，可以继续执行；返回0：消耗检测失败，中断后续执行。
--**********************************
function x300300_OnDeplete( sceneId, selfId )
	local itemTblIndex = LuaFnGetItemIndexOfUsedItem( sceneId, selfId );
	local petItem = x300300_g_petList[itemTblIndex];
	if not petItem then
		x300300_NotifyFailTips(sceneId, selfId, "未开放道具，无法使用。");
		return 0
	end
	--type为1及生产指定ID的珍兽
	if petItem.type == 1 then
