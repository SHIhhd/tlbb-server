--**********************************
--只会执行一次入口：
--聚气和瞬发技能会在消耗完成后调用这个接口（聚气结束并且各种条件都满足的时候），而引导
--技能也会在消耗完成后调用这个接口（技能的一开始，消耗成功执行之后）。
--返回1：处理成功；返回0：处理失败。
--注：这里是技能生效一次的入口
--**********************************
function x300300_OnActivateOnce( sceneId, selfId )
	local itemTblIndex = LuaFnGetItemIndexOfUsedItem( sceneId, selfId );
	local petItem = x300300_g_petList[itemTblIndex];
	if not petItem then
		x300300_NotifyFailTips(sceneId, selfId, "未开放道具，无法使用。");
		return 0;
	end
	--type为1及生产指定ID的珍兽
	if petItem.type == 1 then
		local bCreate = TryCreatePet(sceneId,selfId,2)
		if bCreate <= 0 then
			x300300_NotifyFailTips(sceneId, selfId, "您不能携带更多的珍兽。");
			return 0
		end
		local ret, petGUID_F_H, petGUID_F_L,petGUID_M_H,petGUID_M_L = CallScriptFunction( 800105, "CreateRMBDiffSexPetToHuman", sceneId, selfId, petItem.dataId, petItem.level);
		if ret and ret == 1 then
			x300300_OnGivePlayerPet( sceneId, selfId, petItem.dataId, petGUID_F_H, petGUID_F_L,petGUID_M_H,petGUID_M_L )
			return 1
		end
	end
	return 0
end

