--**********************************
function x300300_LongZiCreatPetPair( sceneId, selfId, nBagPos,nItemIndex,nSelect )
	if nBagPos == nil or nItemIndex == nil or nSelect == nil then
		return
	end
	local itemTblIndex = LuaFnGetItemTableIndexByIndex( sceneId, selfId, nBagPos )
	if itemTblIndex ~= nItemIndex then
		return
	end
	local bCreate = TryCreatePet(sceneId,selfId,2)
	if bCreate <= 0 then
		x300300_NotifyFailTips(sceneId, selfId, "您不能携带更多的珍兽。");
		return
	end
	local petItem = x300300_g_petList[itemTblIndex];
	if not petItem then
		x300300_NotifyFailTips(sceneId, selfId, "未开放道具，无法使用。");
		return
	end
	if petItem.type == 2 then
		if nSelect < 0 or nSelect > getn(petItem.dataIds) or petItem.dataIds[nSelect] == nil or petItem.dataIds[nSelect].dataId == nil then
			x300300_NotifyFailTips(sceneId, selfId, "未开放等级，无法使用。");
			return
		end
		local ret, petGUID_F_H, petGUID_F_L,petGUID_M_H,petGUID_M_L = CallScriptFunction( 800105, "CreateRMBDiffSexPetToHuman", sceneId, selfId, petItem.dataIds[nSelect].dataId, petItem.level);
		if ret and ret == 1 then
			x300300_OnGivePlayerPet( sceneId, selfId, petItem.dataIds[nSelect].dataId, petGUID_F_H, petGUID_F_L,petGUID_M_H,petGUID_M_L )
		end
		LuaFnEraseItem(sceneId,selfId,nBagPos)
	end
end

