	local BagIndex = LuaFnGetBagIndexOfUsedItem(sceneId, selfId)
	local ItemInfo = GetBagItemTransfer( sceneId, selfId, BagIndex )

	if LuaFnDepletingUsedItem( sceneId, selfId ) <= 0 then
		return 0
	end

	local itemTblIndex = LuaFnGetItemIndexOfUsedItem( sceneId, selfId );
	local petItem = x300027_g_petList[itemTblIndex];
	if not petItem then
		x300027_NotifyFailTips(sceneId, selfId, "ЮДПЊЗХЕРОпЃЌЮоЗЈЪЙгУЁЃ");
		return 0;
	end

	local petGUID_H = 0
	local petGUID_L = 0
	local ret = -1
	if petItem.type == 1 then
		ret, petGUID_H, petGUID_L = CallScriptFunction( 800105, "CreateRMBPetToHuman", sceneId, selfId, petItem.dataId, petItem.level);
		if ret and ret == 1 then
			x300027_OnGivePlayerPet( sceneId, selfId, petItem.dataId, petGUID_H, petGUID_L, ItemInfo )
			if itemTblIndex == 30309987 then
				LuaFnSetPetGrowRateByGUID(sceneId,selfId,petGUID_H,petGUID_L,2188)
				local nPetIndex,pet_H,pet_L = -1,0,0
				local nPetNum = LuaFnGetPetCount(sceneId,selfId)
				for i = 0,nPetNum -1 do
					pet_H, pet_L = LuaFnGetPetGUID(sceneId,selfId,i)
					if pet_L == petGUID_L then
						nPetIndex = i
						break;
					end
				end
				LuaFnSetPet_ConPerception(sceneId,selfId,nPetIndex,2400);
			end
			if itemTblIndex == 30309083 then
				LuaFnSetPetGrowRateByGUID(sceneId,selfId,petGUID_H,petGUID_L,2188)
				local nPetIndex,pet_H,pet_L = -1,0,0
				local nPetNum = LuaFnGetPetCount(sceneId,selfId)
				for i = 0,nPetNum -1 do
					pet_H, pet_L = LuaFnGetPetGUID(sceneId,selfId,i)
					if pet_L == petGUID_L then
						nPetIndex = i
						break;
					end
				end
				LuaFnSetPet_ConPerception(sceneId,selfId,nPetIndex,2600);
			end
		end
	elseif petItem.type == 2 then
		local level = GetLevel( sceneId, selfId );
		for i,pet in petItem.dataIds do
			if level >=pet.minHumanLevel and level <= pet.maxHumanLevel then
				ret = CallScriptFunction( 800105, "CreateRMBPetToHuman", sceneId, selfId, pet.dataId, petItem.level);
				if ret and ret == 1 then
					x300027_OnGivePlayerPet( sceneId, selfId, pet.dataId, petGUID_H, petGUID_L, ItemInfo )
				end
			end
		end
	else
	    ret, petGUID_H, petGUID_L = LuaFnCreatePetToHuman(sceneId, selfId, petItem.dataId, petItem.level, 0);
	    if ret and ret == 1 then
				x300027_OnGivePlayerPet( sceneId, selfId, petItem.dataId, petGUID_H, petGUID_L, ItemInfo )
			end
	end
	return 1
end

