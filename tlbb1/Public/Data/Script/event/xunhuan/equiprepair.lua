function x805027_OnEquipRepairofEquip(sceneId,selfId,nEquipPos,targetId)
	if nEquipPos == -1 or nEquipPos == nil then
		x805027_Tirps(sceneId,selfId,"未知错误")
		return
	end
	local nEquipID = LuaFnGetItemTableIndexByIndex(sceneId,selfId,nEquipPos)
	if nEquipID >= x805027_g_SHENQI_BEGIN and nEquipID <= x805027_g_SHENQI_END then
		return
	end
	local price = GetHighRepairPrice(sceneId, selfId,nEquipPos)
	if price < 0 then
		x805027_Tirps(sceneId,selfId,"您的装备等级资料错误，目前无法修理")
		return
	end
	--[巧手木甲]免除一次修理费用
	local nMonsterDataId = GetMonsterDataID(sceneId,targetId)
	if nMonsterDataId == 52202 then
		local nGuildId = LuaFnGetNpcIntParameter(sceneId,targetId,1);
		local nMyGuildId = GetHumanGuildID(sceneId,selfId)
		if nMyGuildId == -1 or nMyGuildId ~= nGuildId then
			x805027_Tirps(sceneId, selfId, "#{SFDJ_240117_46}")
			return
		end
		price = 0
	end
	
	local ret = DoHighRepair( sceneId,selfId,nEquipPos,price)
	if ret == -1 then
		x805027_Tirps(sceneId,selfId,"该装备无法再次修理。")
	elseif ret == -2 then
		x805027_Tirps(sceneId,selfId,"你的银两不能支付修理费用。")
	elseif ret == -4 then
		x805027_Tirps(sceneId,selfId,"修理失败，您的装备可修理次数－1。")
	elseif ret == -5 then
		x805027_Tirps(sceneId,selfId,"修理过程中出现未知错误。")
	elseif ret == -6 then
		x805027_Tirps(sceneId,selfId,"物品没有损害，不用修理。")
	elseif ret == 0 then
		x805027_Tirps(sceneId,selfId,"修理成功！")
		LuaFnSendSpecificImpactToUnit(sceneId,selfId,selfId,selfId,x805027_g_Impact_Complete_Repair,0)
	end
end
