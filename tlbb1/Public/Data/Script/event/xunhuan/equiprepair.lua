--**************************
function x805027_OnEquipRepair(sceneId,selfId,nEnterID,nTarNPCID,nEquipPos,nItemPos)
	if nEquipPos == -1 or nEquipPos == nil or nItemPos == -1 or nItemPos == nil then
		x805027_Tirps(sceneId,selfId,"未知错误")
		return
	end
	local nShenFuLevel = 0;
	local nShenFuId = 0;
	local nShenFuBId = 0;
	local nPrice = GetHighRepairPrice(sceneId,selfId,nEquipPos)
	local nEquipPoint = LuaFnGetBagEquipType(sceneId,selfId,nEquipPos)
	local nSuperWeaponID = LuaFnGetItemTableIndexByIndex(sceneId,selfId,nEquipPos)
	local nMaterialID = LuaFnGetItemTableIndexByIndex(sceneId,selfId,nItemPos)
	for i, item in x805027_g_NeedItemBonus do
		if nSuperWeaponID == item.sqid01
			or nSuperWeaponID == item.sqid02
			or nSuperWeaponID == item.sqid03
			or nSuperWeaponID == item.sqid04 then
			nShenFuId = item.sfid
			--新莽神符没有配等级，根据ID计算等级
			nShenFuLevel = mod(nShenFuId,10) + 1
			break
		end
	end
	if nSuperWeaponID < x805027_g_SHENQI_BEGIN or nSuperWeaponID > x805027_g_SHENQI_END then
		if nShenFuLevel == 7 then
			x805027_Tirps(sceneId,selfId,"#{SQXL_160901_14}")
			else
			x805027_Tirps(sceneId,selfId,ScriptGlobal_Format("#{SQXL_030225_10}", nShenFuLevel))
		end
		return
	end
	
	if nMaterialID ~= 38000055 
	and nMaterialID ~= 38000056 
	and nMaterialID ~= nShenFuId then
		if nShenFuLevel == 7 then
			x805027_Tirps(sceneId,selfId,"#{SQXL_160901_14}")
			else
			x805027_Tirps(sceneId,selfId,ScriptGlobal_Format("#{SQXL_030225_10}", nShenFuLevel))
		end
		return
	end
	
	if nEquipPoint == -1 or nEquipPoint == 8 or nEquipPoint == 9 or nEquipPoint == 10 then
		x805027_Tirps(sceneId,selfId,"此物品不需要修理。")
		return
	end
	-- 15级神器不需要修理 by lishilong 2016-8-23
	if 10300422 == nSuperWeaponID or 10302448 == nSuperWeaponID or 10304426 == nSuperWeaponID
	or 10305444 == nSuperWeaponID or 10306042 == nSuperWeaponID or 10307042 == nSuperWeaponID or 10308042 == nSuperWeaponID then
		x805027_Tirps(sceneId,selfId,"此神器不需要修理")
		return
	end
	if nPrice < 0 then
		x805027_Tirps(sceneId,selfId,"您的神器数据有误，目前无法修理")
		return
	end
	local nRet = DoHighRepair(sceneId,selfId,nEquipPos,nPrice)
	if nRet == -1 then
		x805027_Tirps(sceneId,selfId,"该神器无法再次修理。")
	elseif nRet == -2 then
		x805027_Tirps(sceneId,selfId,"你的银两不能支付修理费用。")
	elseif nRet == -4 then
		x805027_Tirps(sceneId,selfId,"修理失败，您的神器可修理次数－1。")
	elseif nRet == -5 then
		x805027_Tirps(sceneId,selfId,"修理过程中出现未知错误。")
	elseif nRet == -6 then
		x805027_Tirps(sceneId,selfId,"神器没有损害，不用修理。")
	elseif nRet == 0 then
		--扣除发送上来的材料
		LuaFnMtl_CostMaterial(sceneId,selfId,x805027_g_ShenCaiCount,nMaterialID)
		x805027_Tirps(sceneId,selfId,"修理成功！")
		LuaFnSendSpecificImpactToUnit( sceneId, selfId, selfId, selfId, x805027_g_Impact_Complete_Repair, 0 )
	end
end
